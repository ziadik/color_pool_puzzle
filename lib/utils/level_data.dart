  static List<LevelInfo> parseLevels() {
    final lines = rawLevelsData.split('\n');
    var currentLineIndex = 0;
    final levels = <LevelInfo>[];

    while (currentLineIndex < lines.length) {
      final line = lines[currentLineIndex].trim();

      if (line.isEmpty) {
        currentLineIndex++;
        continue;
      }

      final levelNumber = int.tryParse(line);
      if (levelNumber == null) {
        currentLineIndex++;
        continue;
      }

      currentLineIndex++;
      if (currentLineIndex >= lines.length) break;

      final sizeLine = lines[currentLineIndex].trim();
      final sizeParts = sizeLine.split(' ').where((s) => s.isNotEmpty).toList();
      if (sizeParts.length != 2) {
        continue;
      }

      final width = int.parse(sizeParts[0]);
      final height = int.parse(sizeParts[1]);

      final field = List<List<int?>>.generate(
        height,
        (row) => List<int?>.filled(width, null),
      );

      for (int row = 0; row < height; row++) {
        currentLineIndex++;
        if (currentLineIndex >= lines.length) break;

        final rowLine = lines[currentLineIndex].trim();
        final values = rowLine.split(' ').where((s) => s.isNotEmpty).map((s) => int.tryParse(s)).toList();

        for (int col = 0; col < width && col < values.length; col++) {
          field[row][col] = values[col];
        }
      }

      levels.add(LevelInfo(
        levelNumber: levelNumber,
        width: width,
        height: height,
        field: field,
      ));

      currentLineIndex++;
    }

    return levels;
  }
}

class LevelInfo {
  final int levelNumber;
  final int width;
  final int height;
  final List<List<int?>> field;

  LevelInfo({
    required this.levelNumber,
    required this.width,
    required this.height,
    required this.field,
  });
}
